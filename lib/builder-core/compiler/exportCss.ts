export function buildStaticExportCss(theme: GlobalTheme | null | undefined) {
  const vars = buildThemeVars(theme)

  return `/* Generated by Orvenix Builder — orvenix.com.mx */
*, *::before, *::after { box-sizing: border-box; }
${vars ? `body {${vars}
}` : ""}
${buildSharedBaseCss()}`
}

export function buildNextExportGlobalsCss(theme: GlobalTheme | null | undefined) {
  const vars = buildThemeVars(theme)

  return `/* Generated by Orvenix Builder */
@tailwind base;
@tailwind components;
@tailwind utilities;

:root {
${vars}
}

${buildSharedBaseCss()}`
}
