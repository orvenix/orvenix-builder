import type { GlobalTheme } from "../../../types/editor"

function buildThemeVars(theme: GlobalTheme | null | undefined) {
  if (!theme) {
    return ""
  }

  const colors: Partial<NonNullable<GlobalTheme["colors"]>> = theme.colors ?? {}
  const spacing: Partial<NonNullable<GlobalTheme["spacing"]>> = theme.spacing ?? {}
  const radius: Partial<NonNullable<GlobalTheme["radius"]>> = theme.radius ?? {}
  const shadow: Partial<NonNullable<GlobalTheme["shadow"]>> = theme.shadow ?? {}
  const motion: Partial<NonNullable<GlobalTheme["motion"]>> = theme.motion ?? {}

  return `  --primary:    ${colors.primary ?? "#6366f1"};
  --secondary:  ${colors.secondary ?? "#8b5cf6"};
  --background: ${colors.background ?? "#ffffff"};
  --text:       ${colors.text ?? "#1e293b"};
  --accent:     ${colors.accent ?? "#22c55e"};
  --space-section-x: ${spacing.sectionX ?? "1.5rem"};
  --space-section-y: ${spacing.sectionY ?? "3rem"};
  --space-stack: ${spacing.stack ?? "1.5rem"};
  --radius-card: ${radius.card ?? "1rem"};
  --radius-button: ${radius.button ?? "999px"};
  --shadow-soft: ${shadow.soft ?? "0 12px 32px rgba(15,23,42,0.08)"};
  --shadow-strong: ${shadow.strong ?? "0 24px 60px rgba(15,23,42,0.18)"};
  --motion-duration: ${motion.duration ?? "240ms"};
  --motion-easing: ${motion.easing ?? "cubic-bezier(0.22, 1, 0.36, 1)"};
  --font-heading: ${JSON.stringify(theme.fontHeading ?? "system-ui, sans-serif")};
  --font-body:    ${JSON.stringify(theme.fontBody ?? "system-ui, sans-serif")};`
}

